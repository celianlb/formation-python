# Exercice : Crée un programme plus avancé permettant d’afficher l’âge des personnes dans un dictionnaire. 

age = 62;

if age < 18:
    print("Vous êtes mineur.")
elif age >= 18 and age <= 60:
    print("Vous êtes majeur.")
else :
    print("Vous êtes senior")

# Si la valeur de la variable age est inférieure à 18, affichez "Vous êtes mineur.".
# Si la valeur de la variable age est supérieure ou égale à 18 et inférieure ou égale à 60, affichez "Vous êtes majeur.".
# Si la valeur de la variable age est supérieure à 60, affichez "Vous êtes senior.".